
public class Main {
        public static void main(String[] args) {
            int[][] multiarray = { {1, 2}, {3, 4}, {5, 6} };
    
            
            for (int i = 0; i < multiarray.length; i++) {
                for (int j = 0; j < multiarray[i].length; j++) {
                    System.out.print(multiarray[i][j] + " ");
                }
                System.out.println(); 
            }
        }
    }
    

