
public class bitoperator {
    public static void main(String[] args) {
    System.out.println((5 & 6));    // AND (&) operator is used. ans=6
    System.out.println(5|6); // OR (|) operator is used . ans=7
    System.out.println(5^6);// XOR (^) operator is used . ans=3
    System.out.println(~5); // one's complement is used . ans=-6
    System.out.println(5<<2); // binary left shift is used . ans=20 
    System.out.println(6>>1); // binary right shift is used . ans=3
}
 }